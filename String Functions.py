# String Functions

s = "python for life"

# Upper
print(s.upper())

# Lower
print(s.lower())

# String - Boolean Methods

print(s.isalnum())
print(s.isalpha())
print(s.isspace())
print(s.istitle())
print(s.isnumeric())

# String - join(), split(), and replace() Methods

print(s.split())
print(s.replace('for', 'is'))
print(' '.join(s))
