class Calc():
    def __init__(self,a,b,):
        self.a=a
        self.b=b
    def add(self):
        print(self.a+self.b)
    def sub(self):
        print(self.a-self.b)
    def mult(self):
        print(self.a*self.b)
    def div(self):
        print(self.a/self.b)
    def mod(self):
        print(self.a%self.b)
